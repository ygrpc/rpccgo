func TestGenerateJNIRequiresParameters(t *testing.T) {
	plugin := newTestJNIPlugin(t, "paths=source_relative,jni_class=com.example.GreeterJni", simpleTestFile())

	_, err := GenerateJNIWithOptions(plugin)
	if err == nil || !strings.Contains(err.Error(), "rpccgo_header parameter is required") {
		t.Fatalf("GenerateJNIWithOptions() error = %v, want missing rpccgo_header error", err)
	}
}

func TestJNIProtogenOptionsRejectUnknownParameter(t *testing.T) {
	request := newTestCodeGeneratorRequest("jni_class=com.example.GreeterJni,rpccgo_header=librpccgo_service.h,jni_client_dir=android", simpleTestFile())

	_, err := JNIProtogenOptions().New(request)
	if err == nil || !strings.Contains(err.Error(), `unknown rpccgo jni parameter "jni_client_dir"`) {
		t.Fatalf("JNIProtogenOptions().New() error = %v, want unknown parameter error", err)
	}
}

func newTestJNIPlugin(t *testing.T, parameter string, files ...*descriptorpb.FileDescriptorProto) *protogen.Plugin {
	t.Helper()

	request := newTestCodeGeneratorRequest(parameter, files...)
	plugin, err := JNIProtogenOptions().New(request)
	if err != nil {
		t.Fatalf("protogen.Options.New() error = %v", err)
	}
	return plugin
}

func assertJNIGeneratedFilenames(t *testing.T, plugin *protogen.Plugin, want []string) {
	t.Helper()

	got := generatedFilenames(plugin)
	if len(got) != len(want) {
		t.Fatalf("generated files = %v, want %v; response error: %q", got, want, plugin.Response().GetError())
	}
	sort.Strings(got)
	sortedWant := append([]string(nil), want...)
	sort.Strings(sortedWant)
	for i, file := range got {
		if file != sortedWant[i] {
			t.Fatalf("generated file %d = %q, want %q; all files: %v", i, file, sortedWant[i], got)
		}
	}
}
