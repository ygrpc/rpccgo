	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "JNIEXPORT jint JNICALL JNI_OnLoad(JavaVM* vm, void*)")
	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "void* rawEnv = nullptr;")
	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "env = static_cast<JNIEnv*>(rawEnv);")
	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "JNIEnv* attachedEnv = nullptr;")
	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "javaVM->AttachCurrentThread(&attachedEnv, nullptr)")
	assertGeneratedContentContains(t, plugin, "cpp/rpccgo/greeter.greeter.jni.cpp", "Java_com_example_GreeterJni_greeterSayHello")
