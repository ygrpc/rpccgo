func renderDartLookupInitializer(g *protogen.GeneratedFile, file FilePlan, service ServicePlan, method MethodPlan) {
	field := dartFunctionFieldName(method, "")
	switch method.Streaming {
	case StreamingKindUnary:
		g.P(", ", field, " = library.lookupFunction<_RpccgoMessageUnaryNative, _RpccgoMessageUnary>(")
		g.P("'", messageCExportFuncName(file, service, method, ""), "')")
	case StreamingKindClientStreaming:
		renderDartOperationLookupInitializer(g, file, service, method, "start", "_RpccgoStreamStartNative", "_RpccgoStreamStart")
		renderDartOperationLookupInitializer(g, file, service, method, "send", "_RpccgoStreamSendNative", "_RpccgoStreamSend")
		renderDartOperationLookupInitializer(g, file, service, method, "finish", "_RpccgoStreamFinishNative", "_RpccgoStreamFinish")
		renderDartOperationLookupInitializer(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", "_RpccgoStreamCancel")
	case StreamingKindServerStreaming:
		renderDartOperationLookupInitializer(g, file, service, method, "start", "_RpccgoServerStreamStartNative", "_RpccgoServerStreamStart")
		renderDartOperationLookupInitializer(g, file, service, method, "read", "_RpccgoStreamReadNative", "_RpccgoStreamRead")
		renderDartOperationLookupInitializer(g, file, service, method, "finish", "_RpccgoStreamFinishVoidNative", "_RpccgoStreamFinishVoid")
		renderDartOperationLookupInitializer(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", "_RpccgoStreamCancel")
	case StreamingKindBidiStreaming:
		renderDartOperationLookupInitializer(g, file, service, method, "start", "_RpccgoStreamStartNative", "_RpccgoStreamStart")
		renderDartOperationLookupInitializer(g, file, service, method, "send", "_RpccgoStreamSendNative", "_RpccgoStreamSend")
		renderDartOperationLookupInitializer(g, file, service, method, "read", "_RpccgoStreamReadNative", "_RpccgoStreamRead")
		renderDartOperationLookupInitializer(g, file, service, method, "close_send", "_RpccgoStreamFinishVoidNative", "_RpccgoStreamFinishVoid")
		renderDartOperationLookupInitializer(g, file, service, method, "finish", "_RpccgoStreamFinishVoidNative", "_RpccgoStreamFinishVoid")
		renderDartOperationLookupInitializer(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", "_RpccgoStreamCancel")
	}
}

func renderDartOperationLookupInitializer(g *protogen.GeneratedFile, file FilePlan, service ServicePlan, method MethodPlan, operation, nativeType, dartType string) {
	g.P(", ", dartFunctionFieldName(method, operation), " = library.lookupFunction<", nativeType, ", ", dartType, ">(")
	g.P("'", messageCExportFuncName(file, service, method, operation), "')")
}

