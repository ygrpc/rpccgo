// RenderDartGeneratedFiles writes every Dart FFI client artifact in a validated
// generation plan into the plugin response.
func RenderDartGeneratedFiles(plugin *protogen.Plugin, plan GenerationPlan, config DartGeneratorConfig) error {
	if plugin == nil {
		return fmt.Errorf("dart generator plugin is nil")
	}
	if err := ValidateGenerationPlan(plan); err != nil {
		return err
	}
	return renderDartGeneratedFiles(plugin, plan, config)
}

func renderDartGeneratedFiles(plugin *protogen.Plugin, plan GenerationPlan, config DartGeneratorConfig) error {
	renderDartEntryFile(plugin, plan)
	for _, pkg := range plan.Packages {
		for _, file := range pkg.Files {
			for _, service := range file.Services {
				renderDartServiceClientFile(plugin, file, service, config)
			}
		}
	}
	return nil
}

func renderDartEntryFile(plugin *protogen.Plugin, plan GenerationPlan) {
	g := plugin.NewGeneratedFile(defaultDartNativeAssetName, "")
	g.P("// Code generated by protoc-gen-rpc-cgo-dart. DO NOT EDIT.")
	g.P()
	g.P("library;")
	g.P()

	exports := make(map[string]struct{})
	for _, pkg := range plan.Packages {
		for _, file := range pkg.Files {
			exports[file.GeneratedFilenamePrefix+".pb.dart"] = struct{}{}
			for _, service := range file.Services {
				exports[dartServiceClientFilename(file, service)] = struct{}{}
			}
		}
	}

	paths := make([]string, 0, len(exports))
	for export := range exports {
		paths = append(paths, export)
	}
	sort.Strings(paths)
	for _, export := range paths {
		g.P("export '", export, "';")
	}
}

func renderDartServiceClientFile(plugin *protogen.Plugin, file FilePlan, service ServicePlan, config DartGeneratorConfig) {
	filename := dartServiceClientFilename(file, service)
	g := plugin.NewGeneratedFile(filename, "")

	g.P("// Code generated by protoc-gen-rpc-cgo-dart. DO NOT EDIT.")
	g.P("//")
	g.P("// Source: ", file.ProtoPath)
	g.P()
	g.P("@ffi.DefaultAsset('", dartNativeAssetID(config), "')")
	g.P("library;")
	g.P()
	g.P("import 'dart:convert' as convert;")
	g.P("import 'dart:ffi' as ffi;")
	g.P("import 'dart:typed_data' as typed_data;")
	g.P()
	g.P("import 'package:ffi/ffi.dart' as pkg_ffi;")
	g.P()
	g.P("import '", dartPBImport(file), "' as pb;")
	g.P()
	g.P("// rpccgo Dart FFI message client generated file for ", service.GoName)
	g.P()
	renderDartSharedTypedefs(g)
	renderDartSharedNativeBindings(g)
	for _, method := range service.Methods {
		renderDartNativeBinding(g, file, service, method)
	}
	g.P("class ", dartClientClassName(service), " {")
	g.P("const ", dartClientClassName(service), "();")
	for _, method := range service.Methods {
		renderDartClientMethod(g, file, service, method)
	}
	renderDartErrorHelpers(g)
	g.P("}")
	g.P()
	for _, method := range service.Methods {
		renderDartStreamHandleClass(g, service, method)
	}
}

