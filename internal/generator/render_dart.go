func renderDartClientMethod(g *protogen.GeneratedFile, file FilePlan, service ServicePlan, method MethodPlan) {
	switch method.Streaming {
	case StreamingKindUnary:
		renderDartUnaryMethod(g, method)
	case StreamingKindClientStreaming:
		renderDartClientStreamingMethods(g, service, method)
	case StreamingKindServerStreaming:
		renderDartServerStreamingMethods(g, service, method)
	case StreamingKindBidiStreaming:
		renderDartBidiStreamingMethods(g, service, method)
	}
}

func renderDartUnaryMethod(g *protogen.GeneratedFile, method MethodPlan) {
	g.P("pb.", method.Response.GoName, " ", dartMethodName(method), "(pb.", method.Request.GoName, " request) {")
	g.P("final responsePtr = pkg_ffi.calloc<ffi.UintPtr>();")
	g.P("final responseLen = pkg_ffi.calloc<ffi.Int32>();")
	g.P("final requestBytes = request.writeToBuffer();")
	g.P("final requestPtr = _allocateBytes(requestBytes);")
	g.P("try {")
	g.P("final errID = ", dartFunctionFieldName(method, ""), "(requestPtr.address, requestBytes.length, responsePtr, responseLen);")
	g.P("_throwIfError(errID);")
	g.P("final responseBytes = _takeBytes(responsePtr.value, responseLen.value);")
	g.P("return pb.", method.Response.GoName, ".fromBuffer(responseBytes);")
	g.P("} finally {")
	g.P("pkg_ffi.calloc.free(requestPtr);")
	g.P("pkg_ffi.calloc.free(responsePtr);")
	g.P("pkg_ffi.calloc.free(responseLen);")
	g.P("}")
	g.P("}")
	g.P()
}

