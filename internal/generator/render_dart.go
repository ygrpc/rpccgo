func renderDartNativeBinding(g *protogen.GeneratedFile, file FilePlan, service ServicePlan, method MethodPlan) {
	switch method.Streaming {
	case StreamingKindUnary:
		renderDartNativeBindingOperation(g, file, service, method, "", "_RpccgoMessageUnaryNative", dartNativeBindingName(method, ""))
	case StreamingKindClientStreaming:
		renderDartNativeBindingOperation(g, file, service, method, "start", "_RpccgoStreamStartNative", dartNativeBindingName(method, "start"))
		renderDartNativeBindingOperation(g, file, service, method, "send", "_RpccgoStreamSendNative", dartNativeBindingName(method, "send"))
		renderDartNativeBindingOperation(g, file, service, method, "finish", "_RpccgoStreamFinishNative", dartNativeBindingName(method, "finish"))
		renderDartNativeBindingOperation(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", dartNativeBindingName(method, "cancel"))
	case StreamingKindServerStreaming:
		renderDartNativeBindingOperation(g, file, service, method, "start", "_RpccgoServerStreamStartNative", dartNativeBindingName(method, "start"))
		renderDartNativeBindingOperation(g, file, service, method, "read", "_RpccgoStreamReadNative", dartNativeBindingName(method, "read"))
		renderDartNativeBindingOperation(g, file, service, method, "finish", "_RpccgoStreamFinishVoidNative", dartNativeBindingName(method, "finish"))
		renderDartNativeBindingOperation(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", dartNativeBindingName(method, "cancel"))
	case StreamingKindBidiStreaming:
		renderDartNativeBindingOperation(g, file, service, method, "start", "_RpccgoStreamStartNative", dartNativeBindingName(method, "start"))
		renderDartNativeBindingOperation(g, file, service, method, "send", "_RpccgoStreamSendNative", dartNativeBindingName(method, "send"))
		renderDartNativeBindingOperation(g, file, service, method, "read", "_RpccgoStreamReadNative", dartNativeBindingName(method, "read"))
		renderDartNativeBindingOperation(g, file, service, method, "close_send", "_RpccgoStreamFinishVoidNative", dartNativeBindingName(method, "close_send"))
		renderDartNativeBindingOperation(g, file, service, method, "finish", "_RpccgoStreamFinishVoidNative", dartNativeBindingName(method, "finish"))
		renderDartNativeBindingOperation(g, file, service, method, "cancel", "_RpccgoStreamCancelNative", dartNativeBindingName(method, "cancel"))
	}
}

func renderDartNativeBindingOperation(g *protogen.GeneratedFile, file FilePlan, service ServicePlan, method MethodPlan, operation, nativeType, bindingName string) {
	g.P("@ffi.Native<", nativeType, ">(symbol: '", messageCExportFuncName(file, service, method, operation), "')")
	switch method.Streaming {
	case StreamingKindUnary:
		g.P("external int ", bindingName, "(int requestPtr, int requestLen, ffi.Pointer<ffi.UintPtr> responsePtr, ffi.Pointer<ffi.Int32> responseLen);")
	case StreamingKindClientStreaming:
		switch operation {
		case "start":
			g.P("external int ", bindingName, "(ffi.Pointer<ffi.Int32> handle);")
		case "send":
			g.P("external int ", bindingName, "(int handle, int requestPtr, int requestLen);")
		case "finish":
			g.P("external int ", bindingName, "(int handle, ffi.Pointer<ffi.UintPtr> responsePtr, ffi.Pointer<ffi.Int32> responseLen);")
		case "cancel":
			g.P("external int ", bindingName, "(int handle);")
		}
	case StreamingKindServerStreaming:
		switch operation {
		case "start":
			g.P("external int ", bindingName, "(int requestPtr, int requestLen, ffi.Pointer<ffi.Int32> handle);")
		case "read":
			g.P("external int ", bindingName, "(int handle, ffi.Pointer<ffi.UintPtr> responsePtr, ffi.Pointer<ffi.Int32> responseLen);")
		case "finish", "cancel":
			g.P("external int ", bindingName, "(int handle);")
		}
	case StreamingKindBidiStreaming:
		switch operation {
		case "start":
			g.P("external int ", bindingName, "(ffi.Pointer<ffi.Int32> handle);")
		case "send":
			g.P("external int ", bindingName, "(int handle, int requestPtr, int requestLen);")
		case "read":
			g.P("external int ", bindingName, "(int handle, ffi.Pointer<ffi.UintPtr> responsePtr, ffi.Pointer<ffi.Int32> responseLen);")
		case "close_send", "finish", "cancel":
			g.P("external int ", bindingName, "(int handle);")
		}
	}
	g.P()
}

