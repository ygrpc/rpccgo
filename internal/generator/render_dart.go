func renderDartStreamHandleClass(g *protogen.GeneratedFile, service ServicePlan, method MethodPlan) {
	switch method.Streaming {
	case StreamingKindClientStreaming:
		renderDartStreamClass(g, dartClientClassName(service), dartStreamClassName(service, method), method, true, false, true, false)
	case StreamingKindServerStreaming:
		renderDartStreamClass(g, dartClientClassName(service), dartStreamClassName(service, method), method, false, true, false, false)
	case StreamingKindBidiStreaming:
		renderDartStreamClass(g, dartClientClassName(service), dartStreamClassName(service, method), method, true, true, false, true)
	}
}

func renderDartStreamClass(g *protogen.GeneratedFile, clientClassName, className string, method MethodPlan, canSend, canRead, finishReturnsResponse, canCloseSend bool) {
	g.P("class ", className, " {")
	g.P(className, "._(this._client, this._handle);")
	g.P("final ", clientClassName, " _client;")
	g.P("final int _handle;")
	if canSend {
		g.P("void send(pb.", method.Request.GoName, " request) {")
		g.P("final requestBytes = request.writeToBuffer();")
		g.P("final requestPtr = _client._allocateBytes(requestBytes);")
		g.P("try {")
		g.P("final errID = _client.", dartFunctionFieldName(method, "send"), "(_handle, requestPtr.address, requestBytes.length);")
		g.P("_client._throwIfError(errID);")
		g.P("} finally {")
		g.P("pkg_ffi.calloc.free(requestPtr);")
		g.P("}")
		g.P("}")
	}
	if canRead {
		g.P("pb.", method.Response.GoName, " read() {")
		g.P("final responsePtr = pkg_ffi.calloc<ffi.UintPtr>();")
		g.P("final responseLen = pkg_ffi.calloc<ffi.Int32>();")
		g.P("try {")
		g.P("final errID = _client.", dartFunctionFieldName(method, "read"), "(_handle, responsePtr, responseLen);")
		g.P("_client._throwIfError(errID);")
		g.P("final responseBytes = _client._takeBytes(responsePtr.value, responseLen.value);")
		g.P("return pb.", method.Response.GoName, ".fromBuffer(responseBytes);")
		g.P("} finally {")
		g.P("pkg_ffi.calloc.free(responsePtr);")
		g.P("pkg_ffi.calloc.free(responseLen);")
		g.P("}")
		g.P("}")
	}
	if canCloseSend {
		g.P("void closeSend() {")
		g.P("final errID = _client.", dartFunctionFieldName(method, "close_send"), "(_handle);")
		g.P("_client._throwIfError(errID);")
		g.P("}")
	}
	if finishReturnsResponse {
		g.P("pb.", method.Response.GoName, " finish() {")
		g.P("final responsePtr = pkg_ffi.calloc<ffi.UintPtr>();")
		g.P("final responseLen = pkg_ffi.calloc<ffi.Int32>();")
		g.P("try {")
		g.P("final errID = _client.", dartFunctionFieldName(method, "finish"), "(_handle, responsePtr, responseLen);")
		g.P("_client._throwIfError(errID);")
		g.P("final responseBytes = _client._takeBytes(responsePtr.value, responseLen.value);")
		g.P("return pb.", method.Response.GoName, ".fromBuffer(responseBytes);")
		g.P("} finally {")
		g.P("pkg_ffi.calloc.free(responsePtr);")
		g.P("pkg_ffi.calloc.free(responseLen);")
		g.P("}")
		g.P("}")
	} else {
		g.P("void finish() {")
		g.P("final errID = _client.", dartFunctionFieldName(method, "finish"), "(_handle);")
		g.P("_client._throwIfError(errID);")
		g.P("}")
	}
	g.P("void cancel() {")
	g.P("final errID = _client.", dartFunctionFieldName(method, "cancel"), "(_handle);")
	g.P("_client._throwIfError(errID);")
	g.P("}")
	g.P("}")
	g.P()
}

func renderDartErrorHelpers(g *protogen.GeneratedFile) {
	g.P("ffi.Pointer<ffi.Uint8> _allocateBytes(List<int> data) {")
	g.P("final ptr = pkg_ffi.calloc<ffi.Uint8>(data.length);")
	g.P("ptr.asTypedList(data.length).setAll(0, data);")
	g.P("return ptr;")
	g.P("}")
	g.P()
	g.P("typed_data.Uint8List _takeBytes(int ptr, int length) {")
	g.P("if (length < 0) {")
	g.P("throw StateError('rpccgo: response length is negative');")
	g.P("}")
	g.P("if (length == 0) {")
	g.P("return typed_data.Uint8List(0);")
	g.P("}")
	g.P("if (ptr == 0) {")
	g.P("throw StateError('rpccgo: response pointer is nil');")
	g.P("}")
	g.P("final data = typed_data.Uint8List.fromList(ffi.Pointer<ffi.Uint8>.fromAddress(ptr).asTypedList(length));")
	g.P("final status = _release(ptr);")
	g.P("if (status != 0) {")
	g.P("throw StateError('rpccgo: failed to release response buffer');")
	g.P("}")
	g.P("return data;")
	g.P("}")
	g.P()
	g.P("void _throwIfError(int errID) {")
	g.P("if (errID == 0) {")
	g.P("return;")
	g.P("}")
	g.P("throw StateError(_takeError(errID));")
	g.P("}")
	g.P()
	g.P("String _takeError(int errID) {")
	g.P("final textPtr = pkg_ffi.calloc<ffi.UintPtr>();")
	g.P("final textLen = pkg_ffi.calloc<ffi.Int32>();")
	g.P("try {")
	g.P("final status = _takeErrorText(errID, textPtr, textLen);")
	g.P("if (status != 0) {")
	g.P("return 'rpccgo error $errID';")
	g.P("}")
	g.P("final data = _takeBytes(textPtr.value, textLen.value);")
	g.P("return convert.utf8.decode(data);")
	g.P("} finally {")
	g.P("pkg_ffi.calloc.free(textPtr);")
	g.P("pkg_ffi.calloc.free(textLen);")
	g.P("}")
	g.P("}")
}

func dartServiceClientFilename(file FilePlan, service ServicePlan) string {
	return fmt.Sprintf("%s.%s.rpccgo.dart", file.GeneratedFilenamePrefix, lowerSnakeCase(service.GoName))
}

func dartPBImport(file FilePlan) string {
	base := path.Base(file.GeneratedFilenamePrefix)
	return base + ".pb.dart"
}

func dartClientClassName(service ServicePlan) string {
	return service.GoName + "RpccgoClient"
}

func dartStreamClassName(service ServicePlan, method MethodPlan) string {
	return service.GoName + method.GoName + "Stream"
}

func dartMethodName(method MethodPlan) string {
	return method.Name
}

func dartNativeAssetID(config DartGeneratorConfig) string {
	return "package:" + config.DartPackage + "/" + defaultDartNativeAssetName
}

func dartFunctionFieldName(method MethodPlan, operation string) string {
	name := "_" + lowerInitial(method.GoName)
	if operation != "" {
		name += upperCamelFromSnake(operation)
	}
	return name
}

func dartNativeBindingName(method MethodPlan, operation string) string {
	return dartFunctionFieldName(method, operation) + "Raw"
}

func dartSharedNativeBindingName(name string) string {
	return "_rpccgo" + upperCamelFromSnake(name) + "Raw"
}
